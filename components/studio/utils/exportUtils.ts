export const generateReactCode = (exportData: any): void => {
  const { elements, format, includeStyles, minifyCode } = exportData;
  
  let code = `// Generated by Eternal UI Studio
import React from 'react';
${includeStyles ? "import './GeneratedComponent.css';" : ''}

export interface GeneratedComponentProps {
  className?: string;
}

export const GeneratedComponent: React.FC<GeneratedComponentProps> = ({ className }) => {
  return (
    <div className={\`generated-component \${className || ''}\`}>
`;

  elements.forEach((element: any) => {
    code += `      ${generateReactElement(element, 3, includeStyles)}
`;
  });

  code += `    </div>
  );
};

export default GeneratedComponent;`;

  if (minifyCode) {
    code = code.replace(/\s+/g, ' ').trim();
  }

  downloadFile(code, 'GeneratedComponent.tsx', 'text/typescript');
};

