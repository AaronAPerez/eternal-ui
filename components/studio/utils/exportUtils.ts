export const downloadJSON = (exportData: any): void => {
  const jsonData = JSON.stringify(exportData, null, 2);
  downloadFile(jsonData, 'project-export.json', 'application/json');
};

const downloadFile = (content: string, filename: string, mimeType: string): void => {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
};

const generateReactElement = (element: any, indent: number, includeStyles: boolean): string => {
  const indentStr = '  '.repeat(indent);
  const style = includeStyles && element.style 
    ? ` style={${JSON.stringify(element.style)}}` 
    : '';
  
  switch (element.type) {
    case 'text':
      return `${indentStr}<span${style}>${element.props.text || 'Text'}</span>`;
    case 'button':
      return `${indentStr}<button${style}>${element.props.text || 'Button'}</button>`;
    case 'input':
      return `${indentStr}<input type="${element.props.type || 'text'}" placeholder="${element.props.placeholder || ''}"${style} />`;
    default:
      return `${indentStr}<div${style}>{/* ${element.type} */}</div>`;
  }
};

const generateVueElement = (element: any, indent: number, includeStyles: boolean): string => {
  const indentStr = '  '.repeat(indent);
  const style = includeStyles && element.style 
    ? ` :style="${JSON.stringify(element.style)}"` 
    : '';
  
  switch (element.type) {
    case 'text':
      return `${indentStr}<span${style}>{{ ${JSON.stringify(element.props.text || 'Text')} }}</span>`;
    case 'button':
      return `${indentStr}<button${style}>{{ ${JSON.stringify(element.props.text || 'Button')} }}</button>`;
    case 'input':
      return `${indentStr}<input type="${element.props.type || 'text'}" placeholder="${element.props.placeholder || ''}"${style} />`;
    default:
      return `${indentStr}<div${style}><!-- ${element.type} --></div>`;
  }
};

const generateHTMLElement = (element: any, indent: number, includeStyles: boolean): string => {
  const indentStr = '  '.repeat(indent);
  let styleAttr = '';
  
  if (includeStyles && element.style) {
    const styleStr = Object.entries(element.style)
      .map(([key, value]) => `${key.replace(/([A-Z])/g, '-$1').toLowerCase()}: ${value}`)
      .join('; ');
    styleAttr = ` style="${styleStr}"`;
  }
  
  switch (element.type) {
    case 'text':
      return `${indentStr}<span${styleAttr}>${element.props.text || 'Text'}</span>`;
    case 'button':
      return `${indentStr}<button${styleAttr}>${element.props.text || 'Button'}</button>`;
    case 'input':
      return `${indentStr}<input type="${element.props.type || 'text'}" placeholder="${element.props.placeholder || ''}"${styleAttr} />`;
    default:
      return `${indentStr}<div${styleAttr}><!-- ${element.type} --></div>`;
  }
};

